# calculator 01

num01 = int(input("type 1 : "))
oper = str(input("type operator : "))
num02 = int(input("type 2 : "))

if oper == "+":
    print(num01 + num02)
elif oper == "-":
    print(num01 - num02)
elif oper == "*":
    print(num01 * num02)
elif oper == "/":
    print(num01 / num02)
else:
    print("invalid number")












